"""
V3 Prompt Templates for dual-model extraction pipeline.

Design principles:
- "万物皆概念" — anything mentioned in text can be a concept entity
- Maximize recall: phrases, nouns, knowledge, opinions, terms all qualify
- Single comprehensive prompts for capable models (gemma4:26b with think mode)
- Conversational refinement to catch missed concepts
"""

# ============================================================
# V3-1: Comprehensive Entity Extraction
# ============================================================

V3_ENTITY_EXTRACT_SYSTEM = """你是概念提取专家。从文本中提取所有有意义的"概念"。

核心理念：万物皆概念。不论是名词、术语、短语、知识点、观点、流程、机制、事件——只要文本中有提及且有意义，都是一个概念实体。宁可多提取也不要遗漏。"""

V3_ENTITY_EXTRACT_USER = """请仔细阅读以下文本，尽可能完整地提取其中所有有意义的概念。

**提取理念**：万物皆概念。只要是文本中提到的有意义的信息单元，都应提取为概念实体。

**应提取的（包括但不限于）**：
- 具名实体：人名、地名、组织名、作品名、系统名、项目名
- 核心概念：理论、方法、原则、模型、术语、定义、概念
- 过程与机制：流程、步骤、机制、规则、条件、策略
- 技术术语：无论中英文，只要是文本讨论的对象
- 观点与论断：文本表达的观点、结论、判断
- 对比概念：文本中对比讨论的概念对
- 示例实体：文本举例时涉及的实体（如用"小说""主角"举例）
- 属性与特征：概念的具体属性、特征、状态
- 关键词：文本反复出现的关键词语

**实体命名规则**：
- 使用文本中出现的名称。中文概念用中文，英文术语用原文
- 双语标注时只提取一个名称（优先中文），如"概念对齐(Concept alignment hit)"只提取"概念对齐"
- **不要用斜杠"/"组合多个概念**
- 每个概念只提取一次

**极简排除（只排除以下几类）**：
- 纯格式标记（如"```"、"## 标题"等Markdown语法）
- 纯步骤编号（如"Step 1""1️⃣"）
- 代词（他、她、它）

文本：
{window_text}

只输出一个```json```代码块，内部是字符串数组（去重）：
```json
["概念A", "概念B"]
```"""

